#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	i;
	int		len;

	i = 0;
	len = ft_strlen(dest);
	while (i < n && src[i])
		dest[len++] = src[i++];
	dest[len] = '\0';
	return (dest);
}
