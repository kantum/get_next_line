int		ft_factorial(int nb)
{
	if (nb == 0)
		return (1);
	else if (nb > 0 && nb < 13)
	{
		if (nb != 1)
			nb *= ft_factorial(nb - 1);
		return (nb);
	}
	return (0);
}
